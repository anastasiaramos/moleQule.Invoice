-- Bank Lines update
UPDATE "IVBankLine" SET "OID_OPERACION" = PA."OID"
FROM "IVBankLine" AS BL
INNER JOIN "CMCreditCard" AS CC ON CC."OID" = BL."OID_OPERACION" 
	AND BL."TIPO_OPERACION" = 10
INNER JOIN "STPayment" AS PA ON PA."OID_TARJETA_CREDITO" = CC."OID"
	AND PA."TIPO" = 6
	AND PA."VENCIMIENTO" = BL."FECHA_OPERACION"
WHERE "IVBankLine"."OID" = BL."OID";